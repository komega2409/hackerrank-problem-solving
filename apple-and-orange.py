def countApplesAndOranges(s, t, a, b, apples, oranges):
    # Write your code here
    apple_amount = 0
    orange_amount = 0
    for i in range(0, len(apples)):
        apples[i] = apples[i] + a

    for i in range(0, len(oranges)):
        oranges[i] = oranges[i] + b

    for i in apples:
        if s <= i <= t:
            apple_amount += 1
    for i in oranges:
        if s <= i <= t:
            orange_amount += 1
    print(apple_amount)
    print(orange_amount)
